# We can convert the "input()" from a string to an integer by doing casting
n1 = int(input("First number: "))
n2 = int(input("Second number: "))

print()
print("If we check the types now, we can see that both of them are now integers")
print("type(n1) =", type(n1))
print("type(n2) =", type(n2))
print()

total = n1 + n2

print("n1 + n2 =", total)

